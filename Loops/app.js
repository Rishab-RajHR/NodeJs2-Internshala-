// While Loop
let y = 0;
while (y < 10){
    console.log('Value of y '+y);
    y++;
}

// The Do-While Loop is executed at least once even if the condition is false
let z = 100;
do {
   console.log("DO WHILE value of z "+ z);
   z++;
}
while (z<10);